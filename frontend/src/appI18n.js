export function useT() {
  const { language } = useLanguage();
  return useMemo(() => {
    const lang = String(language || '').toLowerCase().slice(0, 2);
    const dict = DICTS_AUTO[lang];
    return (label) => {
      if (!dict) return label;
      return dict[label] || label;
    };
  }, [language]);
}

// Traduction générique par clé. Utilisée par les pages qui ont des chaînes longues
// en plusieurs langues.
export function translate(lang, label) {
  const l = String(lang || '').toLowerCase().slice(0, 2);
  const dict = DICTS_AUTO[l];
  if (!dict) return label;
  return dict[label] || label;
}
