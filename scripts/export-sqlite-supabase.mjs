import Database from 'better-sqlite3';
import path from 'node:path';

const dbPath = path.resolve(process.env.DB_PATH || 'data/intelsheets.db');
const mode = process.argv[2] || 'manifest';
const db = new Database(dbPath, { readonly: true, fileMustExist: true });
db.pragma('foreign_keys = ON');

const quoteIdentifier = value => `"${String(value).replaceAll('"', '""')}"`;
const tables = db.prepare(`SELECT name, sql FROM sqlite_master
  WHERE type = 'table' AND name NOT LIKE 'sqlite_%' ORDER BY name`).all();

function postgresType(type, identity = false) {
  const normalized = String(type || '').toUpperCase();
  if (identity) return 'BIGINT GENERATED BY DEFAULT AS IDENTITY';
  if (normalized.includes('INT')) return 'BIGINT';
  if (normalized.includes('REAL') || normalized.includes('FLOA') || normalized.includes('DOUB') || normalized.includes('NUM')) return 'DOUBLE PRECISION';
  if (normalized.includes('BLOB')) return 'BYTEA';
  if (normalized.includes('BOOL')) return 'BOOLEAN';
  return 'TEXT';
}

function postgresDefault(value) {
  if (value === null || value === undefined) return '';
  const normalized = String(value).trim();
  if (/^\(?datetime\('now'\)\)?$/i.test(normalized) || /^CURRENT_TIMESTAMP$/i.test(normalized)) return ' DEFAULT CURRENT_TIMESTAMP';
  if (/^-?\d+(\.\d+)?$/.test(normalized)) return ` DEFAULT ${normalized}`;
  if (/^NULL$/i.test(normalized)) return ' DEFAULT NULL';
  if (/^'.*'$/.test(normalized)) return ` DEFAULT ${normalized}`;
  return '';
}

function foreignAction(value) {
  const normalized = String(value || 'NO ACTION').toUpperCase();
  return new Set(['NO ACTION', 'RESTRICT', 'SET NULL', 'SET DEFAULT', 'CASCADE']).has(normalized)
    ? normalized
    : 'NO ACTION';
}

function stableName(prefix, table, columns, suffix = '') {
  const raw = `${prefix}_${table}_${columns.join('_')}_${suffix}`.replace(/[^a-zA-Z0-9_]/g, '_');
  return raw.slice(0, 63);
}

function schemaSql() {
  const statements = [];
  for (const table of tables) {
    const columns = db.prepare(`PRAGMA table_info(${quoteIdentifier(table.name)})`).all();
    const primary = columns.filter(column => column.pk).sort((a, b) => a.pk - b.pk);
    const autoIncrement = /AUTOINCREMENT/i.test(String(table.sql || '')) && primary.length === 1;
    const definitions = columns.map(column => {
      const identity = autoIncrement && primary[0].name === column.name;
      const notNull = column.notnull || primary.some(item => item.name === column.name) ? ' NOT NULL' : '';
      const defaultSql = identity ? '' : postgresDefault(column.dflt_value);
      return `${quoteIdentifier(column.name)} ${postgresType(column.type, identity)}${notNull}${defaultSql}`;
    });
    if (primary.length) definitions.push(`PRIMARY KEY (${primary.map(column => quoteIdentifier(column.name)).join(', ')})`);
    statements.push(`CREATE TABLE ${quoteIdentifier(table.name)} (${definitions.join(', ')});`);
  }
  return statements.join('\n');
}

function finalizeSql(offset = 0, limit = tables.length) {
  const statements = [];
  const selectedTables = tables.slice(offset, offset + limit);
  for (const table of selectedTables) {
    const columns = db.prepare(`PRAGMA table_info(${quoteIdentifier(table.name)})`).all();
    const primary = columns.filter(column => column.pk).sort((a, b) => a.pk - b.pk);
    if (/AUTOINCREMENT/i.test(String(table.sql || '')) && primary.length === 1) {
      const column = primary[0].name;
      statements.push(`SELECT setval(pg_get_serial_sequence('${table.name.replaceAll("'", "''")}', '${column.replaceAll("'", "''")}'), COALESCE(MAX(${quoteIdentifier(column)}), 1), MAX(${quoteIdentifier(column)}) IS NOT NULL) FROM ${quoteIdentifier(table.name)};`);
    }

    const indexes = db.prepare(`PRAGMA index_list(${quoteIdentifier(table.name)})`).all();
    const indexedSignatures = new Set();
    for (const index of indexes) {
      if (index.origin === 'pk') continue;
      const indexColumns = db.prepare(`PRAGMA index_info(${quoteIdentifier(index.name)})`).all()
        .sort((a, b) => a.seqno - b.seqno).map(item => item.name).filter(Boolean);
      if (!indexColumns.length) continue;
      indexedSignatures.add(indexColumns.join('\u0000'));
      const generatedName = index.name.startsWith('sqlite_autoindex_')
        ? stableName(index.unique ? 'uq' : 'idx', table.name, indexColumns)
        : index.name.slice(0, 63);
      statements.push(`CREATE ${index.unique ? 'UNIQUE ' : ''}INDEX IF NOT EXISTS ${quoteIdentifier(generatedName)} ON ${quoteIdentifier(table.name)} (${indexColumns.map(quoteIdentifier).join(', ')});`);
    }

    const foreignKeys = db.prepare(`PRAGMA foreign_key_list(${quoteIdentifier(table.name)})`).all();
    const groups = new Map();
    for (const key of foreignKeys) {
      if (!groups.has(key.id)) groups.set(key.id, []);
      groups.get(key.id).push(key);
    }
    for (const [id, keys] of groups) {
      const ordered = keys.sort((a, b) => a.seq - b.seq);
      const foreignColumns = ordered.map(key => key.from);
      const foreignSignature = foreignColumns.join('\u0000');
      if (!indexedSignatures.has(foreignSignature)) {
        const indexName = stableName('idx', table.name, foreignColumns, 'fk');
        statements.push(`CREATE INDEX IF NOT EXISTS ${quoteIdentifier(indexName)} ON ${quoteIdentifier(table.name)} (${foreignColumns.map(quoteIdentifier).join(', ')});`);
        indexedSignatures.add(foreignSignature);
      }
      const constraint = stableName('fk', table.name, ordered.map(key => key.from), String(id));
      statements.push(`ALTER TABLE ${quoteIdentifier(table.name)} ADD CONSTRAINT ${quoteIdentifier(constraint)} FOREIGN KEY (${ordered.map(key => quoteIdentifier(key.from)).join(', ')}) REFERENCES ${quoteIdentifier(ordered[0].table)} (${ordered.map(key => quoteIdentifier(key.to)).join(', ')}) ON UPDATE ${foreignAction(ordered[0].on_update)} ON DELETE ${foreignAction(ordered[0].on_delete)} NOT VALID;`);
      statements.push(`ALTER TABLE ${quoteIdentifier(table.name)} VALIDATE CONSTRAINT ${quoteIdentifier(constraint)};`);
    }

    statements.push(`ALTER TABLE ${quoteIdentifier(table.name)} ENABLE ROW LEVEL SECURITY;`);
    statements.push(`REVOKE ALL ON TABLE ${quoteIdentifier(table.name)} FROM anon, authenticated;`);
    statements.push(`GRANT ALL ON TABLE ${quoteIdentifier(table.name)} TO service_role;`);
  }
  if (offset + limit >= tables.length) {
    statements.push(`INSERT INTO storage.buckets (id, name, public, file_size_limit, allowed_mime_types) VALUES ('message-pdfs', 'message-pdfs', false, 10485760, ARRAY['application/pdf']) ON CONFLICT (id) DO UPDATE SET public = false, file_size_limit = EXCLUDED.file_size_limit, allowed_mime_types = EXCLUDED.allowed_mime_types;`);
  }
  return statements.join('\n');
}

function sqlLiteral(value, columnType) {
  if (value === null || value === undefined) return 'NULL';
  if (Buffer.isBuffer(value)) return `decode('${value.toString('base64')}', 'base64')`;
  if (String(columnType || '').toUpperCase().includes('BOOL')) return value ? 'TRUE' : 'FALSE';
  if (typeof value === 'number') {
    if (!Number.isFinite(value)) return 'NULL';
    return String(value);
  }
  if (typeof value === 'bigint') return value.toString();
  return `'${String(value).replaceAll('\u0000', '\uFFFD').replaceAll("'", "''")}'`;
}

function dataSql(tableName, offset, requestedLimit) {
  const table = tables.find(item => item.name === tableName);
  if (!table) throw new Error(`Table inconnue: ${tableName}`);
  const columns = db.prepare(`PRAGMA table_info(${quoteIdentifier(tableName)})`).all();
  const rows = db.prepare(`SELECT * FROM ${quoteIdentifier(tableName)} LIMIT ? OFFSET ?`).all(requestedLimit, offset);
  const values = [];
  let size = 0;
  for (const row of rows) {
    const encoded = `(${columns.map(column => sqlLiteral(row[column.name], column.type)).join(', ')})`;
    if (values.length && size + encoded.length > 700000) break;
    values.push(encoded);
    size += encoded.length;
  }
  const sql = values.length
    ? `INSERT INTO ${quoteIdentifier(tableName)} (${columns.map(column => quoteIdentifier(column.name)).join(', ')}) VALUES\n${values.join(',\n')}\nON CONFLICT DO NOTHING;`
    : '';
  return { sql, count: values.length, offset, nextOffset: offset + values.length, done: values.length < requestedLimit };
}

try {
  if (db.pragma('quick_check', { simple: true }) !== 'ok') throw new Error('Source SQLite invalide');
  const foreignViolations = db.pragma('foreign_key_check');
  if (foreignViolations.length) throw new Error(`Relations SQLite invalides: ${foreignViolations.length}`);

  if (mode === 'schema') {
    process.stdout.write(JSON.stringify({ sql: schemaSql() }));
  } else if (mode === 'finalize') {
    const offset = Math.max(0, Number(process.argv[3]) || 0);
    const limit = Math.max(1, Number(process.argv[4]) || tables.length);
    process.stdout.write(JSON.stringify({ sql: finalizeSql(offset, limit) }));
  } else if (mode === 'data') {
    const tableName = String(process.argv[3] || '');
    const offset = Math.max(0, Number(process.argv[4]) || 0);
    const limit = Math.min(2000, Math.max(1, Number(process.argv[5]) || 1000));
    process.stdout.write(JSON.stringify(dataSql(tableName, offset, limit)));
  } else {
    const manifest = tables.map(table => ({
      name: table.name,
      rows: db.prepare(`SELECT COUNT(*) AS total FROM ${quoteIdentifier(table.name)}`).get().total,
    }));
    process.stdout.write(JSON.stringify({ tables: manifest, totalRows: manifest.reduce((sum, item) => sum + item.rows, 0) }));
  }
} finally {
  db.close();
}
